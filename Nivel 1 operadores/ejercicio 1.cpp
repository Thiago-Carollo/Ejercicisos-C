#include <iostream>
#include <math.h>
using namespace std;

int main()
{
	float num1, num2, suma, resta, multi, div;
	cout<<"Ingrese 2 numeros: "<<endl;
	cin>>num1;
	cin>>num2;
	suma=num1+num2;
	resta=num1-num2;
	multi=num1*num2;
	div=num1/num2;
	cout<<"El resultado de la suma es: "<<suma<<endl;
	cout<<"El resultado de la resta es: "<<resta<<endl;
	cout<<"El resultado de la multiplicacion es: "<<multi<<endl;
	cout<<"El resultado de la divicion es: "<<div<<endl;
	return 0;
}



